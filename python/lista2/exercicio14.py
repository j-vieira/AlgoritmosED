#Considerando uma hora de viagem
totalHoras = int(input("Coloque a quantidade de horas: "))
totalMinutos = int(input("Coloque a quantidade de minutos: "))
totalSegundos = int(input("Coloque a quantidade de segundos: "))

print(str(totalHoras) + " horas " + str(totalMinutos) + " minutos " + str(totalSegundos) + " segundos é a hora de partida")

print(str(totalHoras+1) + " horas " + str(totalMinutos) + " minutos " + str(totalSegundos) + " segundos é a hora de chegada")